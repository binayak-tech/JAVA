public class RelationalOperators {
    public static void main(String[] args) {
        // Relational operators are --> ==, !=, >, <, >=, <=

        int a, b;
        a = 10;
        b = 12;
        System.out.println((a==b));
        System.out.println((a!=b));
        System.out.println((a>b));
        System.out.println((a<b));
        System.out.println((a>=b));
        System.out.println((a<=b));
     }
}
